     */
    public function edit($id = null)
    {
        if($this->permiso['edit']){

                $collection_count = $this->Collection->find()->where(['Collection.status !=' => '0','Collection.id '=>$id])->count();

                if($collection_count==0){

                                $this->Flash->error(__('Operación denegada.'));
                                return $this->redirect(['action' => 'index']);
                }else{

                $collection = $this->Collection->get($id, [
                    'contain' => []
                ]);

                if ($this->request->is(['patch', 'post', 'put'])) {

                    $collection = $this->Collection->patchEntity($collection, $this->request->getData());

                    if ($this->Collection->save($collection)) {

                        $list_module = explode('/', $this->request->params['_matchedRoute']);

                        $user_id    = $this->Auth->User('id');
                        $module     = $list_module[(count($list_module)-3)];
                        $action     = $list_module[(count($list_module)-2)];
                        $station_id = $collection->id;
                        $recurso_id = '4';

                        $this->Functions->saveLogWeb($module, $station_id, $action, $user_id, $recurso_id);

                        $this->Flash->success(__('Colección actualizada satisfactoriamente.'));

                        return $this->redirect(['action' => 'index']);
                    }

                    $this->Flash->error(__('Hubo inconvenientes al eliminar la colección. Por favor, Otra vez intente.'));

                }

                $scripts = ['assets/packages/jqueryvalidation/dist/jquery.validate'];

                $parent = $this->mod_parent;
                $titulo = $this->mod_padre;

                $recursos = $this->Resource->find('list')->where(['id !=' => '4']);
                $stations = $this->Collection->Station->find('list', ['keyField' => 'id', 'valueField' => 'eea'])->where(['Station.status !=' => 0]);

                $this->set(compact('collection', 'stations', 'titulo', 'parent', 'recursos', 'scripts'));
                $this->set('_serialize', ['collection']);

                }

        } else {

        $this->Flash->error(__('Operación denegada.'));
        return $this->redirect(['action' => 'index']);

        }
    }

    /**
     * Delete method
     *
     * @param string|null $id Collection id.
     * @return \Cake\Network\Response|null Redirects to index.
     * @throws \Cake\Datasource\Exception\RecordNotFoundException When record not found.
     */
    public function delete($id = null)
    {
        if($this->permiso['delete']){

                $collection_count = $this->Collection->find()->where(['Collection.status '=>'1','Collection.id'=>$id])->count();

                if($collection_count>0){

                    $this->request->is(['post', 'delete']);

                    $collection = $this->Collection->find()
                                                 ->where(['Collection.status !=' => '0','Collection.id'=>$id])->first();

                    if($collection==NULL){

                        $this->Flash->error(__('Operación denegada.'));
                        return $this->redirect(['action' => 'index',$id]);

                    }else{

                        $collection['status'] = '0';

                        if ($this->Collection->save($collection)) {

                            $list_module = explode('/', $this->request->params['_matchedRoute']);

                            $user_id    = $this->Auth->User('id');
                            $module     = $list_module[(count($list_module)-3)];
                            $action     = $list_module[(count($list_module)-2)];
                            $station_id = $collection->id;
                            $recurso_id = '4';

                            $this->Functions->saveLogWeb($module, $station_id, $action, $user_id, $recurso_id);

                            $this->Flash->success(__('Colección eliminada satisfactoriamente.'));
                            return $this->redirect(['action' => 'index']);

                        } else {

                            $this->Flash->error(__('Hubo inconvenientes al eliminar la colección. Por favor, Otra vez intente.'));
                            return $this->redirect(['action' => 'index']);
                        }

                    }

                }else{

                    $this->Flash->error(__('Operación denegada.'));
                    return $this->redirect(['action' => 'index']);

                }

        } else {

        $this->Flash->error(__('Operación denegada.'));
        return $this->redirect(['action' => 'index']);

        }
    }

