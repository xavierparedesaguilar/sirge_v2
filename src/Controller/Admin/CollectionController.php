    /**
     * View method
     *
     * @param string|null $id Collection id.
     * @return \Cake\Network\Response|null
     * @throws \Cake\Datasource\Exception\RecordNotFoundException When record not found.
     */
    public function view($id = null)
    {

        if($this->permiso['view']){

                $collection_count = $this->Collection->find()->where(['Collection.status !=' => '0','Collection.id '=>$id])->count();

                if ($collection_count>0) {

                $titulo = $this->mod_padre;
                $parent = $this->mod_parent;
                $permiso= $this->permiso;

                $collection = $this->Collection->get($id, [
                    'contain' => ['Station', 'Resource']
                ]);

                $this->set(compact('collection', 'titulo', 'parent','permiso'));
                $this->set('_serialize', ['collection']);

                }else{

                    $this->Flash->error(__('Operación denegada.'));
                    return $this->redirect(['action' => 'index']);
                }

        } else {

                  $this->Flash->error(__('Operación denegada.'));
                  return $this->redirect(['action' => 'index']);
        }
    }

    /**
     * Add method
     *
     * @return \Cake\Network\Response|null Redirects on successful add, renders view otherwise.
     */
    public function add()
    {

        if($this->permiso['add']){

                $collection = $this->Collection->newEntity();

                if ($this->request->is('post')) {

                    try {

                        $data = $this->request->getData();

                        $data['status'] = '1';

                        $collection = $this->Collection->patchEntity($collection, $data);

                        if ($this->Collection->save($collection)) {

                            $list_module = explode('/', $this->request->params['_matchedRoute']);

                            $user_id    = $this->Auth->User('id');
                            $module     = $list_module[(count($list_module)-2)];
                            $action     = $list_module[(count($list_module)-1)];
                            $station_id = $collection->id;
                            $recurso_id = '4';

                            $this->Functions->saveLogWeb($module, $station_id, $action, $user_id, $recurso_id);

                            $this->Flash->success(__('Colección creado satisfactoriamente.'));

                            return $this->redirect(['action' => 'index']);
                        }

                        $this->Flash->error(__('Hubo inconvenientes al crear la colección. Por favor, Otra vez intente.'));

                    } catch (\Exception $e) {

                        $this->Flash->error(__('Debe completar todos los campos requeridos.'. $e->getMessage()));
                        //return $this->redirect(['action' => 'index']);
                    }
                }

                $scripts = ['assets/packages/jqueryvalidation/dist/jquery.validate'];

                $parent = $this->mod_parent;
                $titulo = $this->mod_padre;

                $recursos = $this->Resource->find('list')->where(['id !=' => '4']);
                $stations = $this->Collection->Station->find('list', ['keyField' => 'id', 'valueField' => 'eea'])->where(['Station.status !=' => 0]);

                $this->set(compact('collection', 'stations', 'titulo', 'parent', 'recursos', 'scripts'));
                $this->set('_serialize', ['collection']);

        } else {
                  $this->Flash->error(__('Operación denegada.'));
                  return $this->redirect(['action' => 'index']);
        }
    }

    /**
     * Edit method
