                    $this->Flash->success(__('La Conservación a Largo Plazo del Material fue eliminado satisfactoriamente.'));
                } else {
                     $this->Flash->error(__('Hubo inconvenientes al eliminar la Conservación a Largo Plazo del Material . Por favor, Otra vez intente.'));
                }

               return $this->redirect(['action' => 'index', $longTermConservationMicro->bank_micro_id]);
            }

        }else{

                $this->Flash->error(__('Operación denegada.'));
                return $this->redirect(['action' => 'index','controller'=>'BankMicro']);

        }
    }

    public function exportartabla($id = null) {

        if ($this->request->is('post')) {

