    /**
     * Add method
     *
     * @return \Cake\Http\Response|null Redirects on successful add, renders view otherwise.
     */
    public function add($id=null)
    {
        $bankMicro_count = $this->BankMicro->find()->where(['BankMicro.status '=>'1','BankMicro.id'=>$id])->count();

        if($bankMicro_count>0 && $this->permiso['add']){


        $longTermConservationMicro = $this->LongTermConservationMicro->newEntity();
        $modulo= $this->mod_padre ;
        $scripts = ['assets/packages/jqueryvalidation/dist/jquery.validate'];
        $longTermConservationMicro->bank_micro_id=$id;

        if ($this->request->is('post')) {

            $data=$this->request->getData();
            $data['status']=1;

            try{
                    $data['bank_micro_id']= $id;
                    $data['constime'] = ($data['fecha_conservacion'] == '' || $data['fecha_conservacion'] == NULL) ? NULL : date('Y-m-d', strtotime($data['fecha_conservacion']));
                    $data['renewal_date'] = ($data['fecha_renovacion'] == '' || $data['fecha_renovacion'] == NULL) ? NULL : date('Y-m-d', strtotime($data['fecha_renovacion']));

                    $longTermConservationMicro = $this->LongTermConservationMicro->patchEntity($longTermConservationMicro,$data);

                    if ($this->LongTermConservationMicro->save($longTermConservationMicro)) {


                            $list_module = explode('/', $this->request->params['_matchedRoute']);

                            $user_id    = $this->Auth->User('id');
                            $module     = $list_module[(count($list_module)-4)];
                            $action     = $list_module[(count($list_module)-2)].'/'.$list_module[(count($list_module)-1)];
                            $station_id = $longTermConservationMicro->id;
                            $recurso_id = '3';

                            $this->Functions->saveLogWeb($module, $station_id, $action, $user_id, $recurso_id);

                            $this->Flash->success(__('La Conservación a Largo Plazo fue creado satisfactoriamente.'));

                            return $this->redirect(['action' => 'index',$longTermConservationMicro->bank_micro_id]);
                    }
                    $this->Flash->error(__('Hubo inconvenientes al crear la Conservación a Largo Plazo. Por favor, Otra vez intente.'));

             } catch (\Exception $e) {

                    $this->Flash->error(__('Hubo inconvenientes al crear la Conservación a Largo Plazo. Por favor, Otra vez intente.'));

                   return $this->redirect(['action' => 'index',$longTermConservationMicro->bank_micro_id]);
            }
        }

        $lista_medio_conservacion= $this->OptionList->find('list', ['keyField' => 'id', 'valueField' => 'name'])->where(['parent_id' => 528, 'status' => 1, 'OR' => [['resource_id' => 3]] ]);

        $lista_crioprotector= $this->OptionList->find('list', ['keyField' => 'id', 'valueField' => 'name'])->where(['parent_id' => 540, 'status' => 1, 'OR' => [['resource_id' => 3]] ]);

        $lista_conservacion= $this->OptionList->find('list', ['keyField' => 'id', 'valueField' => 'name'])->where(['parent_id' => 564, 'status' => 1, 'OR' => [['resource_id' => 3]] ]);

        $lista_lugar_almacenamiento= $this->OptionList->find('list', ['keyField' => 'id', 'valueField' => 'name'])->where(['parent_id' => 558, 'status' => 1, 'OR' => [['resource_id' => 3]] ]);

        $this->set(compact('scripts','longTermConservationMicro','modulo','id','lista_medio_conservacion','lista_crioprotector','lista_conservacion','lista_lugar_almacenamiento'));
        $this->set('_serialize', ['longTermConservationMicro']);

        }else {

                 $this->Flash->error(__('Operación denegada.'));
                 return $this->redirect(['action' => 'index','controller'=>'BankMicro']);
        }
    }

